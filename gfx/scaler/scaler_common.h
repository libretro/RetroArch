#ifndef SCALER_COMMON_H__
#define SCALER_COMMON_H__

#include <stdint.h>

static inline uint8_t clamp_8bit(int val)
{
   if (val > 255)
      return 255;
   else if (val < 0)
      return 0;
   else
      return (uint8_t)val;
}

#endif
