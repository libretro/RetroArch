#define RVP9_LOTS_OF_BITS 0x40000000

static void rvp9_br_fill(rvp9_br *b)
{
   /* libvpx vpx_reader_fill: top up the 64-bit value register; once
    * the buffer is exhausted, inflate count so subsequent reads
    * consume the implicit trailing zeros. When at least 8 bytes
    * remain, take the bulk path: one big-endian load of exactly the
    * bytes the loop would consume (byte k of the load sits at bit
    * 56 - 8k, and >> (count + 8) lands it at 48 - count - 8k, the
    * loop's shift for byte k; masking to nbytes drops the tail bytes
    * the loop would not have loaded). */
   int shift = 64 - 8 - (b->count + 8);
   int bits_left = (int)(b->end - b->buf) * 8;
   int bits_over = shift + 8 - bits_left;
   int loop_end  = 0;
   if (bits_over < 0 && shift >= 0 && b->end - b->buf >= 8)
   {
      int nbytes = shift / 8 + 1;
      uint64_t big;
      memcpy(&big, b->buf, 8);
#if defined(MSB_FIRST)
      /* already big-endian in memory */
#elif defined(__GNUC__)
      big = __builtin_bswap64(big);
#else
      big = ((big & 0x00000000000000FFull) << 56)
          | ((big & 0x000000000000FF00ull) << 40)
          | ((big & 0x0000000000FF0000ull) << 24)
          | ((big & 0x00000000FF000000ull) <<  8)
          | ((big & 0x000000FF00000000ull) >>  8)
          | ((big & 0x0000FF0000000000ull) >> 24)
          | ((big & 0x00FF000000000000ull) >> 40)
          | ((big & 0xFF00000000000000ull) >> 56);
#endif
      big      &= ~0ull << (64 - 8 * nbytes);
      b->value |= big >> (b->count + 8);
      b->buf   += nbytes;
      b->count += nbytes * 8;
      return;
   }
   if (bits_over >= 0)
   {
      b->count += RVP9_LOTS_OF_BITS;
      loop_end  = bits_over;
   }
   if (bits_over < 0 || bits_left)
      while (shift >= loop_end)
      {
         b->count += 8;
         b->value |= (uint64_t)*b->buf++ << shift;
         shift -= 8;
      }
}

/* Returns nonzero on error (bad marker bit). */
static int rvp9_br_read(rvp9_br *b, int prob);
static int rvp9_br_init(rvp9_br *b, const uint8_t *data, size_t size)
{
   b->buf = data; b->end = data + size;
   b->value = 0; b->count = -8; b->range = 255;
   rvp9_br_fill(b);
   return rvp9_br_read(b, 128) != 0;   /* marker bit must be 0 */
}

static int rvp9_br_read(rvp9_br *b, int prob)
{
   unsigned split = (b->range * (unsigned)prob + (256 - (unsigned)prob)) >> 8;
   uint64_t bigsplit;
   unsigned range = split;
   int bit = 0, shift;
   if (b->count < 0) rvp9_br_fill(b);
   bigsplit = (uint64_t)split << (64 - 8);
   if (b->value >= bigsplit)
   {
      range = b->range - split;
      b->value -= bigsplit;
      bit = 1;
   }
   shift = rvp9_norm[(uint8_t)range];
   b->range = range << shift;
   b->value <<= shift;
   b->count -= shift;
   return bit;
}

static int rvp9_br_bit(rvp9_br *b)          { return rvp9_br_read(b, 128); }
static int rvp9_br_lit(rvp9_br *b, int n)
{
   int v = 0, i;
   for (i = 0; i < n; i++) v = (v << 1) | rvp9_br_bit(b);
   return v;
}

/* ==================================================================== */
/* Probability diff updates (vp9_dsubexp.c).                            */
/* ==================================================================== */
static int rvp9_inv_recenter_nonneg(int v, int m)
{
   if (v > 2 * m) return v;
   return (v & 1) ? m - ((v + 1) >> 1) : m + (v >> 1);
}

static int rvp9_decode_uniform(rvp9_br *r)
{
   const int l = 8;
   const int m = (1 << l) - 191;
   const int v = rvp9_br_lit(r, l - 1);
   return v < m ? v : (v << 1) - m + rvp9_br_bit(r);
}

