/* libvpx vp9_seg_feature_data_max / signedness */
static const int rvp9_seg_data_max[RVP9_SEG_LVL_MAX]    = { 255, 63, 3, 0 };
static const int rvp9_seg_data_signed[RVP9_SEG_LVL_MAX] = { 1, 1, 0, 0 };

static int rvp9_rb_unsigned_max(rvp9_rb *rb, int max)
{
   int bits = 0;
   while ((1 << bits) - 1 < max) bits++;  /* get_unsigned_bits */
   return bits ? rvp9_rb_lit(rb, bits) : 0;
}

/* libvpx get_tile_offset(): tile edges are aligned to 64x64
 * superblocks and clamped to the frame. */
static int rvp9_tile_offset(int idx, int mis, int log2)
{
   int sbs = (mis + 7) >> 3;
   int off = ((idx * sbs) >> log2) << 3;
   return off < mis ? off : mis;
}

static void rvp9_get_tile_n_bits(int mi_cols, int *min_log2, int *max_log2)
{
   /* libvpx vp9_get_tile_n_bits: tiles are >=256 and <=4096 px wide */
   const int sb_cols = (mi_cols + 7) >> 3;
   int mn = 0, mx = 0;
   while ((64 << mx) < mi_cols) mx++;      /* max_log2_tile_cols */
   /* min: smallest n with (sb_cols >> n) < 64 ... per libvpx:
    * while ((MAX_TILE_WIDTH_B64 << mn) < sb_cols) mn++;  MAX=64 */
   while ((64 << mn) < sb_cols) mn++;
   /* max: largest n with (sb_cols >> n) >= 4 (MIN_TILE_WIDTH_B64=4) */
   mx = 0;
   while ((sb_cols >> (mx + 1)) >= 4) mx++;
   *min_log2 = mn;
   *max_log2 = mx;
}

static int rvp9_read_frame_size(rvp9_rb *rb, int *w, int *h)
{
   *w = rvp9_rb_lit(rb, 16) + 1;
   *h = rvp9_rb_lit(rb, 16) + 1;
   return 0;
}

static void rvp9_render_size(rvp9_rb *rb, rvp9_hdr *hd)
{
   if (rvp9_rb_bit(rb))
      rvp9_read_frame_size(rb, &hd->render_w, &hd->render_h);
   else
   {
      hd->render_w = hd->w;
      hd->render_h = hd->h;
   }
}

/* Parse the uncompressed header. Returns 0 on success, -1 on failure,
 * 1 for show_existing_frame (no further data).  ref_dims: 3 pairs of
 * w,h for LAST/GOLDEN/ALT (used to resolve size-from-ref); may be NULL
 * when the caller knows the frame cannot reference (keyframes). */
static int rvp9_parse_uncompressed(const uint8_t *data, size_t len,
      rvp9_hdr *hd, const int *ref_dims)
{
   rvp9_rb rb;
   int i;
   rb.data = data; rb.size = len; rb.pos = 0; rb.error = 0;

   {
      /* bit_depth is sequence-sticky (only keyframe / intra_only frames
       * re-signal it); preserve it across the per-frame reset */
      int prev_bd = hd->bit_depth;
      memset(hd, 0, sizeof(*hd));
      hd->bit_depth = prev_bd;
   }

   if (rvp9_rb_lit(&rb, 2) != 2) return -1;      /* frame marker */
   hd->profile  = rvp9_rb_bit(&rb);
   hd->profile |= rvp9_rb_bit(&rb) << 1;
   if (hd->profile > 2) hd->profile += rvp9_rb_bit(&rb);
   /* Profile 0 (8-bit 4:2:0) decodes; profile 2 (10/12-bit 4:2:0)
    * parses fully so callers can identify HDR streams, but
    * reconstruction is gated in rvp9_decode_frame until the
    * high-bit-depth pixel path exists.  Profiles 1/3 (4:4:4 / 4:2:2)
    * are rejected outright. */
   if (hd->profile == 1 || hd->profile >= 3) return -15;
   /* bit_depth is signalled only in keyframe / intra_only color configs;
    * inter frames inherit it, so default it once and keep it sticky. */
   if (hd->bit_depth == 0) hd->bit_depth = 8;

   hd->show_existing_frame = rvp9_rb_bit(&rb);
   if (hd->show_existing_frame)
   {
      hd->frame_to_show = rvp9_rb_lit(&rb, 3);
      return 1;
   }

   hd->frame_type      = rvp9_rb_bit(&rb);
   hd->show_frame      = rvp9_rb_bit(&rb);
   hd->error_resilient = rvp9_rb_bit(&rb);

   if (hd->frame_type == 0)                      /* KEY_FRAME */
   {
      if (rvp9_rb_lit(&rb, 8) != 0x49) return -1;
      if (rvp9_rb_lit(&rb, 8) != 0x83) return -1;
      if (rvp9_rb_lit(&rb, 8) != 0x42) return -1;
      /* color_config: profile 2 carries a ten_or_twelve bit first;
       * 4:2:0 subsampling is implied for both profile 0 and 2. */
      if (hd->profile >= 2)
         hd->bit_depth = rvp9_rb_bit(&rb) ? 12 : 10;
      hd->color_space = rvp9_rb_lit(&rb, 3);
      if (hd->color_space != 7)                  /* != SRGB */
      {
         hd->color_range = rvp9_rb_bit(&rb);
         hd->subsampling_x = hd->subsampling_y = 1;
      }
      else
         return -1;                              /* SRGB needs profile 1/3 */
      hd->refresh_frame_flags = 0xFF;
      rvp9_read_frame_size(&rb, &hd->w, &hd->h);
      rvp9_render_size(&rb, hd);
   }
   else
   {
      hd->intra_only = hd->show_frame ? 0 : rvp9_rb_bit(&rb);
      hd->reset_frame_context =
         hd->error_resilient ? 0 : rvp9_rb_lit(&rb, 2);
      if (hd->intra_only)
      {
         if (rvp9_rb_lit(&rb, 8) != 0x49) return -1;
         if (rvp9_rb_lit(&rb, 8) != 0x83) return -1;
         if (rvp9_rb_lit(&rb, 8) != 0x42) return -1;
         if (hd->profile > 0)
         {
            /* profile 2 intra_only frames carry a full color_config */
            if (hd->profile >= 2)
               hd->bit_depth = rvp9_rb_bit(&rb) ? 12 : 10;
            hd->color_space = rvp9_rb_lit(&rb, 3);
            if (hd->color_space != 7)
            {
               hd->color_range = rvp9_rb_bit(&rb);
               hd->subsampling_x = hd->subsampling_y = 1;
            }
            else
               return -1;                        /* SRGB needs profile 1/3 */
         }
         else
         {
            /* profile 0: BT601 4:2:0 8-bit implied (normative) */
            hd->color_space = 1;
            hd->subsampling_x = hd->subsampling_y = 1;
         }
         hd->refresh_frame_flags = rvp9_rb_lit(&rb, RVP9_REF_FRAMES);
         rvp9_read_frame_size(&rb, &hd->w, &hd->h);
         rvp9_render_size(&rb, hd);
      }
      else
      {
         int found = 0;
         hd->refresh_frame_flags = rvp9_rb_lit(&rb, RVP9_REF_FRAMES);
         for (i = 0; i < RVP9_REFS_PER_FRAME; i++)
         {
            hd->ref_idx[i] = rvp9_rb_lit(&rb, 3);
            hd->ref_sign_bias[1 + i] = rvp9_rb_bit(&rb);
         }
         /* setup_frame_size_with_refs: try each ref's size first */
         for (i = 0; i < RVP9_REFS_PER_FRAME; i++)
         {
            if (rvp9_rb_bit(&rb))
            {
               if (!ref_dims) return -9;
               hd->w = ref_dims[i * 2 + 0];
               hd->h = ref_dims[i * 2 + 1];
               found = 1;
               break;
            }
         }
         if (!found)
            rvp9_read_frame_size(&rb, &hd->w, &hd->h);
         rvp9_render_size(&rb, hd);
         hd->allow_high_precision_mv = rvp9_rb_bit(&rb);
         /* read_interp_filter: 1 bit "switchable", else 2-bit map */
         if (rvp9_rb_bit(&rb))
            hd->interp_filter = 4;               /* SWITCHABLE */
         else
         {
            static const int m[4] = { 1, 0, 2, 3 }; /* literal_to_filter */
            hd->interp_filter = m[rvp9_rb_lit(&rb, 2)];
         }
      }
   }

   if (!hd->error_resilient)
   {
      hd->refresh_frame_context     = rvp9_rb_bit(&rb);
      hd->frame_parallel_decoding   = rvp9_rb_bit(&rb);
   }
   else
   {
      hd->refresh_frame_context   = 0;
      hd->frame_parallel_decoding = 1;
   }
   hd->frame_context_idx = rvp9_rb_lit(&rb, 2);

   /* loop filter */
   hd->lf_level     = rvp9_rb_lit(&rb, 6);
   hd->lf_sharpness = rvp9_rb_lit(&rb, 3);
   hd->lf_delta_enabled = rvp9_rb_bit(&rb);
   for (i = 0; i < 4; i++) hd->lf_ref_delta_upd[i] = 0;
   for (i = 0; i < 2; i++) hd->lf_mode_delta_upd[i] = 0;
   if (hd->lf_delta_enabled)
   {
      hd->lf_delta_update = rvp9_rb_bit(&rb);
      if (hd->lf_delta_update)
      {
         for (i = 0; i < 4; i++)
            if (rvp9_rb_bit(&rb))
            {
               hd->lf_ref_deltas[i] = rvp9_rb_slit(&rb, 6);
               hd->lf_ref_delta_upd[i] = 1;
            }
         for (i = 0; i < 2; i++)
            if (rvp9_rb_bit(&rb))
            {
               hd->lf_mode_deltas[i] = rvp9_rb_slit(&rb, 6);
               hd->lf_mode_delta_upd[i] = 1;
            }
      }
   }

   /* quantization */
   hd->base_qindex   = rvp9_rb_lit(&rb, 8);
   hd->y_dc_delta_q  = rvp9_rb_bit(&rb) ? rvp9_rb_slit(&rb, 4) : 0;
   hd->uv_dc_delta_q = rvp9_rb_bit(&rb) ? rvp9_rb_slit(&rb, 4) : 0;
   hd->uv_ac_delta_q = rvp9_rb_bit(&rb) ? rvp9_rb_slit(&rb, 4) : 0;
   hd->lossless = (hd->base_qindex == 0 && hd->y_dc_delta_q == 0 &&
                   hd->uv_dc_delta_q == 0 && hd->uv_ac_delta_q == 0);

   /* segmentation */
   hd->seg_enabled = rvp9_rb_bit(&rb);
   if (hd->seg_enabled)
   {
      hd->seg_update_map = rvp9_rb_bit(&rb);
      if (hd->seg_update_map)
      {
         for (i = 0; i < RVP9_SEG_TREE_PROBS; i++)
            hd->seg_tree_probs[i] = (uint8_t)
               (rvp9_rb_bit(&rb) ? rvp9_rb_lit(&rb, 8) : 255);
         hd->seg_temporal_update = rvp9_rb_bit(&rb);
         if (hd->seg_temporal_update)
            for (i = 0; i < RVP9_PRED_PROBS; i++)
               hd->seg_pred_probs[i] = (uint8_t)
                  (rvp9_rb_bit(&rb) ? rvp9_rb_lit(&rb, 8) : 255);
         else
            for (i = 0; i < RVP9_PRED_PROBS; i++)
               hd->seg_pred_probs[i] = 255;
      }
      hd->seg_update_data = rvp9_rb_bit(&rb);
      if (hd->seg_update_data)
      {
         int s, f;
         hd->seg_abs_delta = rvp9_rb_bit(&rb);
         for (s = 0; s < RVP9_MAX_SEGMENTS; s++)
            for (f = 0; f < RVP9_SEG_LVL_MAX; f++)
            {
               int dat = 0;
               int en = rvp9_rb_bit(&rb);
               hd->seg_feature_enabled[s][f] = en;
               if (en)
               {
                  dat = rvp9_rb_unsigned_max(&rb, rvp9_seg_data_max[f]);
                  if (rvp9_seg_data_signed[f] && rvp9_rb_bit(&rb))
                     dat = -dat;
               }
               hd->seg_feature_data[s][f] = dat;
            }
      }
   }

   /* tiles */
   {
      int min_l2, max_l2, max_ones;
      hd->mi_cols = (hd->w + 7) >> 3;
      hd->mi_rows = (hd->h + 7) >> 3;
      hd->sb_cols = (hd->mi_cols + 7) >> 3;
      hd->sb_rows = (hd->mi_rows + 7) >> 3;
      rvp9_get_tile_n_bits(hd->mi_cols, &min_l2, &max_l2);
      max_ones = max_l2 - min_l2;
      hd->log2_tile_cols = min_l2;
      while (max_ones-- && rvp9_rb_bit(&rb)) hd->log2_tile_cols++;
      hd->log2_tile_rows = rvp9_rb_bit(&rb);
      if (hd->log2_tile_rows) hd->log2_tile_rows += rvp9_rb_bit(&rb);
   }

   hd->first_partition_size = rvp9_rb_lit(&rb, 16);
   hd->uncomp_size = (rb.pos + 7) >> 3;
   return rb.error ? -1 : 0;
}

