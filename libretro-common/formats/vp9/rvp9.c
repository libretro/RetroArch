/* Compressed header. Returns tx_mode, or -1 on error.  For inter
 * frames (hd->frame_type==1 && !intra_only) also reads the inter
 * probability updates; reference_mode et al returned via *d. */
static int rvp9_parse_compressed_intra(const uint8_t *data, size_t sz,
      const rvp9_hdr *hd, rvp9_frame_ctx *fc, rvp9_br *r_out)
{
   rvp9_br r;
   int tx_mode, i, j, k, l, m;
   static const int band_ctx[RVP9_COEF_BANDS] = { 3, 6, 6, 6, 6, 6 };
   static const int biggest_tx[5] = { 0, 1, 2, 3, 3 };

   rvp9_br_init(&r, data, sz);

   if (hd->lossless)
      tx_mode = 0;
   else
   {
      tx_mode = rvp9_br_lit(&r, 2);
      if (tx_mode == 3) tx_mode += rvp9_br_bit(&r);
   }
   if (tx_mode == 4)   /* TX_MODE_SELECT: tx size probs */
   {
      for (i = 0; i < RVP9_TX_SIZE_CONTEXTS; i++)
         for (j = 0; j < 1; j++) rvp9_diff_update_prob(&r, &fc->tx8[i][j]);
      for (i = 0; i < RVP9_TX_SIZE_CONTEXTS; i++)
         for (j = 0; j < 2; j++) rvp9_diff_update_prob(&r, &fc->tx16[i][j]);
      for (i = 0; i < RVP9_TX_SIZE_CONTEXTS; i++)
         for (j = 0; j < 3; j++) rvp9_diff_update_prob(&r, &fc->tx32[i][j]);
   }
   /* coef probs per tx size up to the mode's biggest */
   for (i = 0; i <= biggest_tx[tx_mode]; i++)
   {
      if (rvp9_br_bit(&r))
         for (j = 0; j < RVP9_PLANE_TYPES; j++)
            for (k = 0; k < RVP9_REF_TYPES; k++)
               for (l = 0; l < RVP9_COEF_BANDS; l++)
               {
                  int c;
                  for (c = 0; c < band_ctx[l]; c++)
                     for (m = 0; m < RVP9_UNCONSTRAINED; m++)
                        rvp9_diff_update_prob(&r,
                           &fc->coef[i][j][k][l][c][m]);
               }
   }
   for (i = 0; i < RVP9_SKIP_CONTEXTS; i++)
      rvp9_diff_update_prob(&r, &fc->skip[i]);

   if (r_out) *r_out = r;
   return tx_mode;
}

