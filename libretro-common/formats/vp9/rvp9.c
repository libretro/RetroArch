static int rvp9_inv_remap_prob(int v, int m)
{
   v = rvp9_inv_map_table[v];
   m--;
   if ((m << 1) <= 255)
      return 1 + rvp9_inv_recenter_nonneg(v, m);
   return 255 - rvp9_inv_recenter_nonneg(v, 255 - 1 - m);
}

static int rvp9_decode_term_subexp(rvp9_br *r)
{
   if (!rvp9_br_bit(r)) return rvp9_br_lit(r, 4);
   if (!rvp9_br_bit(r)) return rvp9_br_lit(r, 4) + 16;
   if (!rvp9_br_bit(r)) return rvp9_br_lit(r, 5) + 32;
   return rvp9_decode_uniform(r) + 64;
}

#define RVP9_DIFF_UPDATE_PROB 252

static void rvp9_diff_update_prob(rvp9_br *r, uint8_t *p)
{
   if (rvp9_br_read(r, RVP9_DIFF_UPDATE_PROB))
   {
      const int delp = rvp9_decode_term_subexp(r);
      *p = (uint8_t)rvp9_inv_remap_prob(delp, *p);
   }
}

