/* ==================================================================== */
/* Public teardown.                                                     */
/* ==================================================================== */
void rvp9_free(rvp9_dec *d)
{
   int i;
   if (!d)
      return;
   for (i = 0; i < RVP9_FRAME_BUFS; i++)
   {
      free(d->fbs[i].y);
      free(d->fbs[i].u);
      free(d->fbs[i].v);
      d->fbs[i].y = d->fbs[i].u = d->fbs[i].v = NULL;
   }
   free(d->mi);
   free(d->prev_mvs);
   free(d->cur_mvs);
   free(d->above_seg);
   for (i = 0; i < 3; i++)
   {
      free(d->above_ctx[i]);
      d->above_ctx[i] = NULL;
   }
   d->mi = NULL;
   d->prev_mvs = d->cur_mvs = NULL;
   d->above_seg = NULL;
   d->buf_y = d->buf_u = d->buf_v = NULL;
}
