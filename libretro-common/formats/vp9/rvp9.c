/* ==================================================================== */
/* Bit reader for the uncompressed header (MSB-first, byte stream).     */
/* Mirrors libvpx vpx_read_bit_buffer.                                  */
/* ==================================================================== */
typedef struct
{
   const uint8_t *data;
   size_t         size;
   size_t         pos;    /* bit position */
   int            error;
} rvp9_rb;

static int rvp9_rb_bit(rvp9_rb *rb)
{
   size_t p = rb->pos;
   size_t off = p >> 3;
   if (off >= rb->size) { rb->error = 1; return 0; }
   rb->pos = p + 1;
   return (rb->data[off] >> (7 - (p & 7))) & 1;
}

static int rvp9_rb_lit(rvp9_rb *rb, int bits)
{
   int v = 0, i;
   for (i = 0; i < bits; i++)
      v = (v << 1) | rvp9_rb_bit(rb);
   return v;
}

static int rvp9_rb_slit(rvp9_rb *rb, int bits)
{
   int v = rvp9_rb_lit(rb, bits);
   return rvp9_rb_bit(rb) ? -v : v;
}

