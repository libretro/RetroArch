/* End-to-end regression coverage for the webm audio path
 * (rwebm demux -> Ogg synthesis -> rvorbis decode).
 *
 * Exists because of a real silent-audio regression: commit 639a4df
 * added a setup-time residue-range check capping end at
 * blocksize_1 / 2, but libvorbis legally writes type-2
 * (channel-coupled) residue ranges over the interleaved ch*n vector -
 * up to twice the half-block for ordinary stereo - so every routine
 * stereo encode was rejected with RVORBIS_invalid_setup and webm audio
 * went silent, while mono kept working.  The fix (clamp at decode, as
 * upstream stb_vorbis does; bound the header field at
 * 2 * (blocksize_1 >> 1)) is pinned here from both directions:
 *
 *  - FIX_STEREO is a stock libvorbis stereo encode whose residue 1 has
 *    end = 1600 against blocksize_1/2 = 1024: exactly the legal shape
 *    the bad check rejected.  It must decode, with the exact frame
 *    count and byte-exact 16-bit PCM (FNV-1a hash) recorded from the
 *    fixed decoder.  Any hash change means decoded output changed:
 *    that is sometimes intentional, but it must be a conscious
 *    decision - re-verify against a known-good decoder before updating
 *    the constant.
 *
 *  - FIX_MONO pins the uncoupled path the same way.
 *
 *  - The malformed case reuses FIX_STEREO with 4 bytes of its residue
 *    1 begin/end bit-fields forced to 0xFF (end reads back 16777215),
 *    which must still be REJECTED: this pins the stack-safety intent
 *    of the original hardening commit.
 *
 * Run this before shipping any change to rvorbis.c, rwebm.c or
 *  rwebm_audio.c:
 *
 *    gcc -std=gnu99 -O2 -I../../include test_rwebm_audio.c -lm && ./a.out
 *
 * The decoder sources are included directly below with HAVE_RVORBIS
 * defined here, so the test cannot silently compile the Vorbis path
 * out (a mis-set define produced a bogus bisect while diagnosing the
 * original regression). */

